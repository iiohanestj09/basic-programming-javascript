// For Loop
for (let i = 0; i < 5; i++) {
    console.log(`Perulangan For Ke-${i+1}`);
}


// While Loop
let count = 3;
while (count > 0) {
    console.log(`Hitung Mundur: ${count}`);
    count--;
}

// Do While
let attempt = 0;
do {
    console.log("Menjalankan proses...");
    attempt++;
} while (attempt < 3);